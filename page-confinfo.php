    <div class="col-md-6">
    <h2>Getting stuff done and finding who to talk to</h2>
    <p>New to ownCloud, looking for an introduction or answers about API's, infrastructure or a pull request?
    <br/> <strong>ask your questions in <em>starters room</em> <code>MA642</code></strong></p>
    <p>If you want to make sure a certain topic is discussed, make an agreement with the relevant people and <a href="https://github.com/owncloud/core/wiki/Conference-meetings">book the meeting room</a>.</p>
    </div>
</div>

