    </div>
    <div class="col-md-6">
    <h2>Finding your way</h2>
    <h3>Hackathon</h3>
    <p>Friday, registration and hacking rooms open at 9. Walk from registration desk to the elevators. This is where we are:
    <ul>
        <li>5th floor:  food-and-drink room <code>MA542</code> and <a href="https://github.com/owncloud/core/wiki/Conference-meetings">meeting room</a> <code>MA548</code></li>
        <li>6th floor the hacking rooms: <code>MA642</code> (<em>starters room</em>), <code>MA644</code>, <code>MA648</code>, <code>MA649</code> and <code>MA650</code></li>
        <li>8th floor:interview room (<code>MA841</code>)</li>
        <li>All the way to the top and up the stairs one more floor for lunch (with your food ticket!) and a great view over the city in the restaurant</li>
    </ul>
    </p>
    <h3>Conference days - Saturday and Sunday</h3>
    <p>Registration opens at 9 and at 10 we start with opening, keynotes and lightning talks in room <code>MA004</code>, main hall of the "<em>Mathematik</em>" building. After lunch the hacking rooms open while the main hall features workshops in the <code>MA004</code>, <code>MA005</code> and <code>MA144</code> rooms.</p>
    </div>
</div>

<div class="row conf-info">
