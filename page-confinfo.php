    <h2>Registration and badge</h2>
    <p>Be sure to have <a href="https://conference.owncloud.org/conference/oCC2015/register/new">registered in advance for the conference</a>! Pick up your badge and some food tickets at the entree of the "<em>Mathematik</em>" building, Strasse des 17. Juni 136.</br>
