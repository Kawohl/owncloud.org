	<h1>ownCloud Support options</h1>
</div>
<p>ownCloud is deployed in a wide variety of situations. Home users run it on a NAS or Raspberry Pi, using Calendar and Contact synchronization and many more of the <a href="https://apps.owncloud.com" target="_blank">more than 200 community apps</a>. Enterprises, research institutions and government agencies deploy it on large server parks, requiring integration in existing enterprise infrastructure like SAML and Sharepoint.</p>
<p>We provide information on how to install and maintain ownCloud for everybody <a href="htts://doc.owncloud.org" target="_blank">in our documentation</a>. When more resources are needed, private user can find help in our <a href="http://localhost:5000/faq/#install" target="_blank">community help channels</a> while professional users benefit from <a href="http://owncloud.com" target="_blank">self help resources, enterprise functionality and paid support options from ownCloud, Inc.</a></p>

